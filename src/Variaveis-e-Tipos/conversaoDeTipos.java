public class conversaoDeTipos {
    public static void main(String[] args) {
        double valorDouble = 19.5;
        int valorInt = (int) valorDouble; // Conversão explícita (casting)
        System.out.printf("Valor double: %.2f\n", valorDouble);
        System.out.printf("Valor int (após conversão): %d\n", valorInt);
    }
}
